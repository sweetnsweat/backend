숨은 채로 그의 말과 움직임을 주의 깊게 관찰하던 중, 황태자가 성소 한쪽 벽에 걸린 오래된 문서와 상징들을 유심히 살피는 모습을 포착한다. 문서에는 당신과 관련된 상징과 이름들이 적혀 있었고, 황태자는 그것을 보며 잠시 말을 잇지 못했다. 이때, 그의 입가에 미묘한 흔들림이 생기고, 마음속 깊은 곳에서 당신에 대한 혼란과 갈등이 일고 있음을 알 수 있다. 그는 당신이 단순한 ‘적’이 아니라는 사실을 점차 깨닫기 시작한다.

당신이 숨은 채로 관찰하는 동안, 황태자는 성소 안에 남겨진 오래된 흔적들을 통해 잃어버린 성녀 후보에 관한 비밀을 일부 수집한다. 그러나 그가 스스로 입을 열어 설명하지 않는 한, 당신은 여전히 그의 진심을 완전히 파악하지 못한 상태다.

결국 황태자는 더 이상의 불필요한 긴장을 피하기 위해 조용히 성소 밖으로 나가려는 움직임을 보인다. 그는 마지막으로 낮고 단호한 목소리로 말한다. “나를 믿을 준비가 되거든, 다시 찾아라. 그때는 숨지 말고, 직접 마주하자.” 그의 말투에는 앞으로의 만남에 대한 무언의 약속과 함께, 앞으로 당신과 그 사이에 놓일 복잡한 관계의 가능성이 담겨 있다.

