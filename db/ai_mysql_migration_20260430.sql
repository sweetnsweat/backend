성소 내부는 오래된 신전답게 고요하면서도 신비로운 분위기가 감돌았다. 먼지 쌓인 제단과 벽면에 새겨진 낡은 문양, 그리고 곳곳에 흩어진 낡은 서적들이 그녀의 발걸음을 이끌었다. 황태자는 한걸음 뒤에서 조용히 지켜보며, 그녀가 조사하는 모습을 신중하게 관찰했다. 그의 눈빛에는 의심과 호기심이 뒤섞여 있었지만, 강압적인 태도는 보이지 않아 긴장은 계속됐지만 적대감은 일단 잦아들었다.

그녀는 먼저 제단 아래에서 반쯤 묻힌 작은 상자를 발견했다. 상자를 열자, 황금빛으로 빛나는 오래된 메달과 함께, 손때 묻은 두루마리가 나왔다. 두루마리에는 ‘잃어버린 성녀’를 암시하는 듯한 고대 문자가 적혀 있었고, 그녀는 문자를 해독할 수 없었지만, 메달에는 황태자와 비슷한 문양이 새겨져 있어 묘한 연결고리를 느꼈다. 이 순간, 황태자는 조용히 다가와 “그 메달은 황실의 성물 중 하나입니다. 네가 정말 성녀 후보라면, 네 과거와도 깊은 연관이 있을 거야.”라고 말했다.

하지만 그녀가 두루마리를 들여다보는 사이, 성소 안의 어딘가에서 약간의 흔들림과 함께 먼지가 떨어지며, 오래된 성소가 점차 불안정해지고 있음을 알리듯 벽 틈새에서 미세한 균열이 생겼다. 황태자는 급히 “여기 오래된 곳이라 위험할 수 있어요. 더 이상 머무르기 어렵겠네요.”라며 성급히 손을 내밀었지만, 그녀는 단서를 더 찾고 싶다는 의지를 굽히지 않았다.

이 탐색 과정에서 그녀는 황태자에 대한 경계심을 더욱 키웠고, 황태자는 그녀의 냉정한 태도에 불편함을 느끼면서도 그만큼 신뢰를 얻기 위한 진심 어린 태도를 보이려 노력했다. 이로써 두 사람 사이에는 미묘한 긴장감과 함께, 서로를 시험하는 듯한 감정의 씨앗이 뿌려졌다. 또한, 그녀가 직접 과거의 단서를 찾으려는 태도는 이후 그녀가 자신의 운명을 스스로 개척하려는 독립적인 면모를 드러내며, 황태자와의 관계가 단순한 권위와 복종의 관계를 넘어서서 점차 복잡해지는 갈등과 협력의 기반이 되었다.

성소 탐색을 마친 후, 그녀는 메달과 두루마리를 품에 안고 황태자를 바라보며 조용히 말했다. “이 단서가 내 과거에 어떤 의미가 있는지, 당신과 함께 밝혀내야 할 것 같아요. 하지만 경계는 늦추지 않을 겁니다.” 황태자는 고개를 끄덕이며, “그럴 수밖에 없겠죠. 하지만 함께라면 더 많은 진실을 찾을 수 있을 겁니다.”라고 답했다.

