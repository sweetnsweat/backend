하지만 동시에, 당신이 직접 기억에 대해 묻는 돌직구는 황태자에게도 부담으로 작용했다. 그의 표정은 순간 흔들렸고, 당신에 대한 경계심과 의심이 완전히 사라지지는 않았다. 이 때문에 두 사람 사이에는 아직 완전한 신뢰가 자리 잡지 못했고, 앞으로의 여정에서 서로를 시험하는 일이 잦을 것임이 예고되었다.

이 대화는 앞으로 당신이 기억을 찾아가는 과정에서 황태자가 결정적인 조력자이자 동시에 미묘한 라이벌이 될 가능성을 남겼다. 그리고 황태자가 알려준 황실 내부의 음모는, 당신이 성소를 벗어나 다시 세상과 맞서야 할 때 큰 시련이 될 것이 분명했다.

